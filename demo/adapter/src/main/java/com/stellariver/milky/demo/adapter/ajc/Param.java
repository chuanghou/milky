package com.stellariver.milky.demo.adapter.ajc;

public class Param {

    private String name;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Param{" +
                "name='" + name + '\'' +
                '}';
    }

}
